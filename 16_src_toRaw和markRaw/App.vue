<template>
  <Demo v-if="isShowDemo"/>
  <button @click="isShowDemo = !isShowDemo">点击切换 Demo</button>
</template>

<script>
import {ref} from 'vue'
import Demo from './components/Demo'
export default {
  name: 'App',
  components: { Demo },
  setup() {
    let isShowDemo = ref(true)
    return {
      isShowDemo
    }
  }
}
</script>
